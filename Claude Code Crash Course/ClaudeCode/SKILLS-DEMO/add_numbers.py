def add_numbers(num1, num2):
    """Add two numbers and return the result.

    Args:
        num1: First number
        num2: Second number

    Returns:
        The sum of num1 and num2
    """
    return num1 + num2


if __name__ == "__main__":
    result = add_numbers(5, 3)
    print(f"5 + 3 = {result}")
